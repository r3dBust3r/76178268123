int	ft_str_is_numeric(char *str)
{
	char	c;
	int		i;

	i = 0;
	while (str[i])
	{
		c = str[i];
		if (!(c >= 48 && c <= 57))
		{
			return (0);
		}
		i++;
	}
	return (1);
}

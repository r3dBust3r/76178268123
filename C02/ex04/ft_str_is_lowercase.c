int	ft_str_is_lowercase(char *str)
{
	char	c;
	int		i;

	i = 0;
	while (str[i])
	{
		c = str[i];
		if (!(c >= 97 && c <= 122))
		{
			return (0);
		}
		i++;
	}
	return (1);
}

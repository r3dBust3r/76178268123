int	ft_sqrt(int nb)
{
	int	pw;
	int	i;

	i = 1;
	pw = 0;
	while (1)
	{
		pw = i * i;
		if (pw == nb)
			return (i);
		if (pw > nb)
			return (0);
		i++;
	}
}
